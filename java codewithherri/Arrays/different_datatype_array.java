public class different_datatype_array {
    public static void main(String[] args) {
        int[] marks={55,77,88,99,66};
        System.out.println(marks.length);
        System.out.println(marks[3]);

        float[] marks1={55.5f,77.89f,88.25f,99.28f,66.76f,89.56f};
        System.out.println(marks1.length);
        System.out.println(marks1[3]);

        String[] student={"mantu","hari","ashish","yuvraj","sryansh","mukul","ankit"};
        System.out.println(student.length);
        System.out.println(student[0]);

    }
}
